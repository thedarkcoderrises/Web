package com.citi.dde.achweb.pojo;

public class LogInUserDetails {

	private String userName;
	private String userRole;
	private boolean viewRole;
	private boolean dualRole;
	private boolean makerRole;
	private boolean checkerRole;
	
	
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the userRole
	 */
	public String getUserRole() {
		return userRole;
	}
	/**
	 * @param userRole the userRole to set
	 */
	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}
	/**
	 * @return the viewRole
	 */
	public boolean isViewRole() {
		return viewRole;
	}
	/**
	 * @param viewRole the viewRole to set
	 */
	public void setViewRole(boolean viewRole) {
		this.viewRole = viewRole;
	}
	/**
	 * @return the dualRole
	 */
	public boolean isDualRole() {
		return dualRole;
	}
	/**
	 * @param dualRole the dualRole to set
	 */
	public void setDualRole(boolean dualRole) {
		this.dualRole = dualRole;
	}
	/**
	 * @return the makerRole
	 */
	public boolean isMakerRole() {
		return makerRole;
	}
	/**
	 * @param makerRole the makerRole to set
	 */
	public void setMakerRole(boolean makerRole) {
		this.makerRole = makerRole;
	}
	/**
	 * @return the checkerRole
	 */
	public boolean isCheckerRole() {
		return checkerRole;
	}
	/**
	 * @param checkerRole the checkerRole to set
	 */
	public void setCheckerRole(boolean checkerRole) {
		this.checkerRole = checkerRole;
	}
	
	
	

}
